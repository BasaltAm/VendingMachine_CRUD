public class Vender {

    private int price;
    private int change;

    public int getPrice() {
        return price;
    }

    public int setPrice(int price) {
        this.price = price;
        return price;
    }

    public int getChange() {
        return change;
    }

    public int setChange(int change) {
        this.change = change;
        return change;
    }

}
